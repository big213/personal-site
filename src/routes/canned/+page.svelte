  <h1>Canned Food</h1>
</div>
<hr />
<p>
  I eat a lot of canned foods as part of my normal diet, which is something many
  people find bizarre about me. See below for some pictures of what I normally
  eat on a daily basis. There are some variations here and there based on the
  availability and cost of ingredients (I also try to keep my food costs
  reasonable without compromising on quality).
</p>
<p>
  Please note that not all ingredients shown are canned, but everything is
  prepared with use of a microwave or hot water kettle. I also get some
  perishables and non-canned ingredients from websites or local grocery stores,
  and I eat some fresh fruits and string cheese throughout the day as a snack.
</p>
<p>
  Disclaimer: I do not necessarily recommend that you follow what I do, but this
  diet has worked for me so far since I started around January 2022. I think
  canned ingredients are great for a variety of reasons, and I encourage people
  to use more of them as part of their daily cooking.
</p>
